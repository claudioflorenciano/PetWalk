    public void formataFrame(int idCliente, int idPasseador){
        cliente = ctrlCliente.pesquisaCliente(idCliente);
        passeador = ctrlPasseador.pesquisaPasseador(idPasseador);
        
        jTextFieldNomeWalkPro.setText(passeador.getNome());
        
        pets = ctrlCliente.pesquisaAnimais(cliente.getId_cliente());
        
        jComboBoxPetCadWalk.removeAllItems();
        if(!pets.isEmpty()){
            for(Animal p: pets){
                jComboBoxPetCadWalk.addItem(p.getNome());
            }
        }
        double vh = (passeador.getValorHora() / 60) * Double.parseDouble(jFormattedTextFieldCadWalkDuracao.getText());
        jTextFieldCadWalkPreco.setText("R$ "+ String.format("%.2f", vh));
        
    }
    
