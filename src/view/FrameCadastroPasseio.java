    private void jButtonConfirmaCadWalkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonConfirmaCadWalkMouseClicked
        // TODO add your handling code here:
        //cadastro de passeio
        CtrlPasseio ctrlPasseio = new CtrlPasseio();
        Animal animal = new Animal();
        Passeio passeio = new Passeio(cliente, animal, passeador);
        
        int idSelectedAnimal = jComboBoxPetCadWalk.getSelectedIndex();
        
        passeio.getAnimal().setId_animal(pets.get(idSelectedAnimal).getId_animal());
        
        String formataData = jFormattedTextFieldCadWalkDataHora.getText();
        
        passeio.setDuracaoPasseio(Integer.parseInt(jFormattedTextFieldCadWalkDuracao.getText()));
        String preco = jTextFieldCadWalkPreco.getText().replace("R", "");
        preco = preco.replace("$","");
        preco = preco.replace(",", ".");
        preco = preco.trim();
        passeio.setPrecoPasseio(Float.parseFloat(preco));
 
        try{
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
            LocalDateTime data = LocalDateTime.parse(formataData, formatter);
            
            passeio.setAgendamentoPasseio(data);
            
            if((passeio.getPrecoPasseio() > 0) && data.isAfter(LocalDateTime.now())){
                passeio.setStatus(true);
                if(ctrlPasseio.cadastraPasseio(passeio)){
                    JOptionPane.showMessageDialog(this, "Passeio agendado com sucesso!", "Sucesso!", JOptionPane.WIDTH, iconCheck);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Ocorreu um erro no agendamento!", "Aviso!", JOptionPane.WIDTH, iconErro);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Por favor insira os dados corretamente!", "Aviso!", JOptionPane.WIDTH, iconErro);
            }
            
        }catch(DateTimeParseException e){
            JOptionPane.showMessageDialog(this, "Data inválida!", "Aviso!", JOptionPane.WIDTH, iconErro);
        }  
        
        
    }//GEN-LAST:event_jButtonConfirmaCadWalkMouseClicked

